    public static synchronized void getTran()  {
        try {
            ResultSet rs=MiscTools.sta.executeQuery("SELECT * FROM TRANRECYCLE ORDER BY TRANID;");
            if(rs.next())  {
                tranID=rs.getInt("TranID");
                return;
            }
            rs=MiscTools.sta.executeQuery("SELECT COUNT(TRANSACTIONID) FROM TRANSACTION;");
            rs.next();
            tranID=(rs.getInt(1)+1);
        }catch(SQLException e)  {
                JOptionPane.showMessageDialog(null, e);
                tranID=-1;
        }
    }
    @Override
    public void run()  {
        try {
            while(!Thread.currentThread().isInterrupted())  {
                txtAmount.setText(String.valueOf(MiscTools.checkAmt(txtUser.getText()))); 
                if(Double.parseDouble(txtAmount.getText())==0)  
                    Thread.currentThread().interrupt();
                Thread.sleep(5000);
            }
        }catch(InterruptedException e) {
            if(this.isDisplayable())  {
                JOptionPane.showMessageDialog(null,"Something's wrong with the account!");
                this.dispose();
            }
        }
    }
