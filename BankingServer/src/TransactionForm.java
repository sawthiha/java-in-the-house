    public static synchronized void messClean()  {
        try {
            Statement s=MiscTools.c.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_READ_ONLY);
            ResultSet rs=s.executeQuery("SELECT TRANID FROM TRANRECYCLE WHERE TRANID="+tranID+";");
            if(rs.next())
            s.executeUpdate("DELETE * FROM TRANRECYCLE WHERE TRANID="+tranID+";");
            s.close();
        }catch(SQLException e)  {}
        getTran();
    }
