    public void tableProcess()  {
        try {
            row=0;
            PreparedStatement ps=MiscTools.c.prepareStatement("SELECT * FROM TRANSACTION WHERE USERID='"+txtUserID.getText()+"';");
            ResultSet rs=ps.executeQuery();
            DefaultTableModel dm=new DefaultTableModel();
            String[] rowData=new String[5];
            String[] columnData={"Transaction No","Transaction Date","Transaction Type","Transaction Amount","To"};
            for(String str:columnData)  {
                dm.addColumn(str); 
            }
            while(rs.next())  {
                rowData[0]=String.valueOf(rs.getInt("TransactionID")); //no
                rowData[1]=rs.getString("DOT"); //date
                rowData[2]=rs.getString("Type"); //type
                rowData[3]=String.valueOf(rs.getDouble("Amount")); //amount
                rowData[4]=rs.getString("Destination");//to
                dm.addRow(rowData);
                row++;
            }
            tblTran.setModel(dm); 
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
