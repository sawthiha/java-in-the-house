    @Override
    public void run() {
        try {
            while(!Thread.currentThread().isInterrupted())  {
                if(MiscTools.checkTransaction(txtUserID.getText())==row);
                else if(MiscTools.checkTransaction(txtUserID.getText())==-1)  {
                    JOptionPane.showMessageDialog(null,"Something's wrong with Account!");
                    Thread.currentThread().interrupt();
                } else {
                    tableProcess();
                }
                Thread.sleep(5000); 
            }
        }catch(InterruptedException e)  {
            if(this.isVisible())  {
                this.dispose();
            }
        }
    }
